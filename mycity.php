                <p style="color:#3C4043;line-height: 2;font-size:15px;text-align:JUSTIFY"><b>Some Important Historical Events</b>
                 <br>   
                 The Sultanpur district Gazeteer published in 1903 A.D. throws some light on the history and origine of the district. It is seen that the chief land owning families of the past were the Rajputs of the various clans, who possessed 76.16 percent of the total land area. Among them the Rajkumars along-held over one-fourth of the district, while their kinsmen, the Bachgotis and Rajwars owned 11.4 and 3.4 percent, respectively. The Rajkumars were the proprietors of nearly the whole of Aldemau. Their chief was the Raja of Deara. The head of Bachgotis was the Raja of Kurwar while the taluqdar of Samrathpur represented another branch of the family. The chief of Rajwars was the taluqdar of Pratabpur. Another member of the Rajwars family was the Raja of Hasanpur. Allied to him were the families of Maniarpur and Gangeo and between them they owned a large portion of the central area. Next to Bachgotis and their kinsmen come the Bandhalgotis, who owned almost the whole of Amethi pargana. Their head was the Raja of Amethi while the taluqdar Shahgarh belonged to the same clan. The Rajputs with large properties in the district were the Bhale Sultans who owned 4.72 percent, the Kanhapurias with 4.7 percent, and the Bais with 2.8 percent. Of the Bhale Sultans half were Hindus and half Mussalmans. They were dwelling in the north west corner of the district in the parganas of Isauli, Musafirkhana and Jagdishpur. The Kanhpurias were chiefly confined to pargana Gaura Jamo, almost the whole of which belonged to them. The Bais were scattered about in small groups.<br>
                 Another important branch of the land owning clans was the house of Raj Sah. Raj Sah had three sons, Ishri Singh, Chakrasen Singh and Rup Chand. From Ishari Singh, after nine generations came Bijai Chand, who had three sons. Harkaran Deo. Jit Rai, and Jionarain. Harkaran Deo was the ancestor of Nanemau taluqdar; the descendants of Jit Rai were the owners of Meopur Dahla, Meopur Dhaurua, and Bhadaiyan; and from Jionarain descended the Raja of Deara. The fourth descendant of Jionarain led the the first of the six colonies of Rajkumars across the Gomti and planted himself at Deara on the banks of the river. This house became one of the main branches of the Bachgotis of Sultanpur.<br>
                 At the begining of the nineteenth century Babu Madho Singh, eleventh in descent from Jionarain was the rular of the estate which consisted of 101 villages. Babu Madho Singh who is remembered as the successful leader and who managed his property well died in 1823. He was succeded by his widow, Thakurain Dariao Kunwar, a most remarkable woman, who through toil and turmoil not only bravely held her own, but added to her estates than her husband had done in his life time. The direct line of succession had ended with the death of Thakurain’s husband, Babu Madho singh. The Next male collateral heir was Babu Rustam Sah, whom Thakurain disliked. Babu Rustam Sah was in the service of Maharaja Man Singh, the nazim of the day and with his help succeded in capturing Thakurain and made her write a deed in his favour. That formidable woman, whose pride was hurt grieved for a few months and died. Rustam Sah was given the possession of the property by the nazim. Rustam Sah came to know later that the nazim had ulterior motives in helping him. A fight would have followed and Rustam would have killed nazim, but for a pandit who advised him that the time was not propitious. Later, Rustam Sah sought asylum across the British border and was made the taluqdar of Deara, which consisted of 336 villages. Rustam Sah rendered excellent service during the Mutiny. He died in 1877 and was succeded by his nephew, Raja Rudra Pratap Singh.<br>
                 Bariar Singh, the youngest brother of Rustam Sah, received an estate of 20 villages and three pattis in the parganas of Baraunsa and Aldemau in return for services rendered during the Mutiny. This property was known as Damodra or Sultanpur.<br>
                 Sultanpur has been a historic contribution in the development of national consciousness. In the independence struggle of 1857, the revolutionaries took a pledge from the British to put an end to the overthrow of British imperialism. In the peasant movement of 1921, this district has been openly participating and the bravery and courage of the freedom fighter of Yeha in all the movements from 1930 to 1942, and later, is historic.Farmer leader Baba Ram Chandra and Baba Ram Lal are remarkable in this context, whose description of renunciation is mentioned. Jawaharlal Nehru has done in his autobiography.<br>
                 All these local rajas were under the control of Dilli hukumat and nawabs of Avadh.
